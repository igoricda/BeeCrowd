#include <stdio.h>
 
int main() {
 
    //Declaração de variáveis
    int a, b, c, d, diff;
    
    // Inputs
    scanf("%d %d %d %d", &a, &b, &c, &d);
    
    //Cálculo
    diff = ((a * b) - (c * d));
    
    //Output
    printf("DIFERENCA = %d\n", diff);
    
 
    return 0;
}
